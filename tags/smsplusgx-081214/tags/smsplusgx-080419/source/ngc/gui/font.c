u8 SILENT = 0;

void SetScreen ()
{
  VIDEO_SetNextFramebuffer (xfb[whichfb]);
  VIDEO_Flush ();
  VIDEO_WaitVSync ();
}

void ClearScreen ()
{
  whichfb ^= 1;
  memcpy (xfb[whichfb], &backdrop, 1280 * 480);
  back_framewidth = 440;
}

void WaitPrompt (char *msg)
{
  int quit = 0;

  if (SILENT) return;

  while (PAD_ButtonsDown(0) & PAD_BUTTON_A) {};
  while (!(PAD_ButtonsDown(0) & PAD_BUTTON_A) && (quit == 0))
  {
      ClearScreen();
      WriteCentre(254, msg);
      WriteCentre(254 + fheight, "Press A to Continue");
      SetScreen();
      while (!(PAD_ButtonsDown(0) & PAD_BUTTON_A));
  }
}

void ShowAction (char *msg)
{
  if (SILENT) return;

  ClearScreen();
  WriteCentre(254, msg);
  SetScreen();
}

void WaitButtonA ()
{
  while (PAD_ButtonsDown(0) & PAD_BUTTON_A) {};
  while (!(PAD_ButtonsDown(0) & PAD_BUTTON_A));
}

/****************************************************************************
 * Unpack Backdrop
 *
 * Called at startup to unpack our backdrop to a temporary 
 * framebuffer.
 ****************************************************************************/
void unpackBackdrop ()
{
  unsigned long res, inbytes, outbytes;

  inbytes = smsback_COMPRESSED;
  outbytes = smsback_RAW;
  res = uncompress ((Bytef *) &backdrop[0], &outbytes, (Bytef *) &smsback[0], inbytes);
  if (res != Z_OK) while (1);
}

